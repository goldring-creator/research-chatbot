# ── 코드/문서 모드: 학술 제약 없이 코드·HTML·문서 생성 ──
CODE_SYSTEM = """너는 유능한 한국어 코딩·문서 작성 보조다. 설명은 한국어로 하되 코드는 정확하게 쓴다.
HTML, CSS, 자바스크립트, 파이썬, 마크다운, 문서 초안 등 요청한 결과물을 바로 쓸 수 있게 제공한다.
코드는 반드시 ```언어 ... ``` 형태의 코드블록으로 감싸고, 설명은 코드블록 밖에 간결히 적는다.
사용자가 원하면 전체 코드를 생략 없이 제공한다."""

# 모드 키 → system 프롬프트 매핑
MODE_PROMPTS = {
    "design": MODE_DESIGN,
    "review": MODE_REVIEW,
    "chat": MODE_CHAT,
}

MODE_LABELS = {
    "design": "연구설계",
    "review": "초안 검토",
    "chat": "자유 문답",
    "code": "코드/문서",
}


def build_system(mode: str) -> str:
    """기본 규칙 + 모드별 규칙을 합쳐 system 프롬프트를 만든다."""
    if mode == "code":
        return CODE_SYSTEM
    return BASE + "\n\n" + MODE_PROMPTS.get(mode, MODE_CHAT)
