# 모드 키 → system 프롬프트 매핑
MODE_PROMPTS = {
    "design": MODE_DESIGN,
    "review": MODE_REVIEW,
    "chat": MODE_CHAT,
}

MODE_LABELS = {
    "design": "연구설계",
    "review": "초안 검토",
    "chat": "자유 문답",
}


def build_system(mode: str) -> str:
    """기본 규칙 + 모드별 규칙을 합쳐 system 프롬프트를 만든다."""
    return BASE + "\n\n" + MODE_PROMPTS.get(mode, MODE_CHAT)
